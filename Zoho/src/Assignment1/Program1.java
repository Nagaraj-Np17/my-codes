package Assignment1;
// Write a program to demonstrate compatible type conversions. For eg., float to int, double to float, int to short
public class Program1 {

	public static void main(String[] args) {
		double val=300.0;
		System.out.println("Double to Float  ="+(float)val);
		System.out.println("Float to Integer ="+(int)val);		
		System.out.println("Integer to short  ="+(short)val);
		System.out.println("shot to byte ="+(byte)val);

	}

}
